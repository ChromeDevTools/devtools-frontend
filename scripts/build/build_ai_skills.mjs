import yaml from 'js-yaml';
import * as fs from 'node:fs';
import * as path from 'node:path';

import {writeIfChanged} from './ninja/write-if-changed.js';

/**
 * Parses a skill markdown file content to extract frontmatter and instructions.
 *
 * @param {string} content The markdown file content.
 * @returns {{name: string|undefined, description: string|undefined, allowedTools: string[], instructions: string}} The parsed skill object.
 */
export function parseSkill(content) {
  // Matches files starting with YAML frontmatter delimited by '---'.
  // Captures frontmatter in group 1 and the rest of the file in group 2.
  const match = content.match(/^---\r?\n([\s\S]*?)\r?\n---\r?\n([\s\S]*)$/);

  /** @type {{name?: string, description?: string, 'allowed-tools'?: string[]}} */
  let frontmatter = {};
  let instructions = content;

  if (match) {
    try {
      frontmatter = yaml.load(match[1]) || {};
      instructions = match[2];
    } catch (e) {
      throw new Error(`Failed to parse YAML frontmatter: ${e.message}`);
    }
  }

  return {
    name: frontmatter.name,
    description: frontmatter.description,
    allowedTools: frontmatter['allowed-tools'] || [],
    instructions: instructions.trim()
  };
}

function runMain() {
  const [, , buildTimestamp, inputFile, outputFile] = process.argv;
  const year = new Date(Number(buildTimestamp) * 1000).getUTCFullYear();

  const content = fs.readFileSync(inputFile, 'utf-8');
  const skill = parseSkill(content);

  const outputContent = `// Copyright ${year} The Chromium Authors. All rights reserved.
// Use of this source code is governed by a BSD-style license that can be
// found in the LICENSE file.
// IMPORTANT: this file is auto generated. Please do not edit this file.

export const skill = ${JSON.stringify(skill, null, 2)};
`;

  // Ensure target directory exists
  fs.mkdirSync(path.dirname(outputFile), {recursive: true});

  writeIfChanged(outputFile, outputContent);
}

if (import.meta.main) {
  runMain();
}
